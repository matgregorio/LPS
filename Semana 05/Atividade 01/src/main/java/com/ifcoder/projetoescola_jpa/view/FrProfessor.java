    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Professor professorExcluido = (Professor) this.getObjetoSelecionadoNaGrid();

        if (professorExcluido == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {

            int response = JOptionPane.showConfirmDialog(null,
                    "Deseja exlcuir o Professor  \n("
                    + professorExcluido.getNome() + ", "
                    + professorExcluido.getCpf() + ") ?",
                    "Confirmar exclusão",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.OK_OPTION) {

                try {
                    profController.excluirProfessor(cpfProfessorEditando);

                    profController.atualizarTabela(grdProfessor);
                    JOptionPane.showMessageDialog(this, "Exclusão feita com sucesso!");
                } catch (ProfessorException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Professor professorEditando = (Professor) this.getObjetoSelecionadoNaGrid();
    
        if (professorEditando == null) 
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.preencherFormulario(professorEditando);
            this.cpfProfessorEditando = professorEditando.getCpf();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private Object getObjetoSelecionadoNaGrid() {
        int rowCliked = grdProfessor.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdProfessor.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }
     
